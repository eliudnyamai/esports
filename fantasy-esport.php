								<h2>Qu’est-ce que le Fantasy eSport ?</h2>
								<p>
                                Le Fantasy eSport est la version en ligne de la ligue fantasy. En résumé, c’est un type de sport électronique qui regroupe plusieurs autres eSport dits fantastiques.
                                </p>
                                <p>
                                Pour précisément les novices, Fantasy eSport se décline comme une compétition dont le but est de créer une <strong>équipe sportive </strong> et d’en endosser la propriété. En d’autres termes, chaque joueur met en exergue ses compétences pour mettre en place une <strong>Fantasy team </strong>dans l’optique d’affronter d’autres équipes.
                                </p>
                                <p>
                                La particularité de ce type de <strong>sport électronique </strong>  réside dans la <strong>composition des équipes</strong>. En effet, chacune d’elles est constituée de vrais joueurs (physique) et non de personnages virtuels. Le joueur créateur de l’équipe en est également l’entraîneur. Il dispose d’un compte monétaire virtuel qui lui permet <strong>d'acheter ou de vendre un membre de son équipe</strong>.
                                </p>
                                <p>
                                En ce qui concerne le système de récompense, il repose sur un principe d’accumulation de point afin de finir au top 1 du classement en ligne. Les performances des joueurs sont également prises en compte pour déterminer <strong>le vainqueur d’un tournoi.</strong> 
                                </p>
